import { BodyText } from 'components/Text'
import { Suspense } from 'react'
import { useSnapshot } from 'valtio'
import ContractListContainer from 'components/ContractListContainer'
import ProofStore from 'stores/ProofStore'
import ZKProof from 'components/ZKProof'

function ContractList() {
  const { proofsCompleted } = useSnapshot(ProofStore)

  return (
    <>
      {!!proofsCompleted?.length && (
        <ContractListContainer>
          {Array.from(proofsCompleted)
            .sort((a, b) => {
              if (a.account === b.account) return 0
              if (a.account !== b.account) return -1
              return 1
            })
            .map((proof) => (
              <ZKProof proof={proof} key={proof.id} />
            ))}
        </ContractListContainer>
      )}
    </>
  )
}

export default function ListOfReadyZKProofs() {
  return (
    <Suspense
      fallback={
        <BodyText size="base">Fetching proofs generated by you...</BodyText>
      }
    >
      <ContractList />
    </Suspense>
  )
}
